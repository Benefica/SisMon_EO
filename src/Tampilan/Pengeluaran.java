    public void tabelPengeluaran(){
        Object header[]={"ID PENGELUARAN","ID PEMBAYARAN","NAMA CLIENT","BIAYA EVENT","BIAYA PERLENGKAPAN","BIAYA KONSUMSI","TANGGAL PENGELUARAN"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        tabelPengeluaran.setModel(data);
        setKoneksi();
        String sql="select*from data_pengeluaran";
        try {
            ResultSet rs=stm.executeQuery(sql);
            while (rs.next())
            {
                String kolom1=rs.getString("id_pengeluaran");
                String kolom2=rs.getString("id_pembayaran");
                String kolom3=rs.getString("nama_client");
                String kolom4=rs.getString("biaya_event");
                String kolom5=rs.getString("biaya_perlengkapan");
                String kolom6=rs.getString("biaya_konsumsi");
                String kolom7=rs.getString("tanggal_pengeluaran");
                
                String kolom[]={kolom1,kolom2,kolom3,kolom4,kolom5,kolom6,kolom7};
                data.addRow(kolom);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "data gagal dipanggil"+e);
        }
    }
    
    public void Tampil_ID() {
        try {
            ResultSet rs = stm.executeQuery("SELECT * FROM data_pengeluaran ORDER BY id_pengeluaran DESC");

            if (rs.next()) {
                int lastID = Integer.parseInt(rs.getString("id_pengeluaran").substring(3));
                int newID = lastID + 1;
                String AN = String.format("%04d", newID);
                tIdPengeluaran.setText("PGL" + AN);
            } else {
                tIdPengeluaran.setText("PGL0001");
            }
        } catch (Exception e) {
            tIdPengeluaran.setText("PGL0001");
        }
    }
