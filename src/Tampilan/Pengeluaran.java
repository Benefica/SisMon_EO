    private void tabelPengeluaranMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelPengeluaranMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat SDF = new SimpleDateFormat("yyyy-MM-dd");
        Date TGL = null;
        int baris = tabelPengeluaran.getSelectedRow();
        try {
             TGL = SDF.parse((String) tabelPengeluaran.getValueAt(baris, 6));
             } catch (ParseException ex) {
                Logger.getLogger(Pengeluaran.class.getName()).log(Level.SEVERE, null, ex);
            }
        tIdPengeluaran.setText(tabelPengeluaran.getModel().getValueAt(baris, 0).toString());
        tIdPembayaran.setText(tabelPengeluaran.getModel().getValueAt(baris, 1).toString());
        tNamaClient.setText(tabelPengeluaran.getModel().getValueAt(baris, 2).toString());
        tBiayaEvent.setText(tabelPengeluaran.getModel().getValueAt(baris, 3).toString());
        tBiayaPerlengkapan.setText(tabelPengeluaran.getModel().getValueAt(baris, 4).toString());
        tBiayaKonsumsi.setText(tabelPengeluaran.getModel().getValueAt(baris, 5).toString());
        datePengeluaran.setDate(TGL);
    }//GEN-LAST:event_tabelPengeluaranMouseClicked

