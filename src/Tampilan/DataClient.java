    private void tabel_clientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_clientMouseClicked
        // TODO add your handling code here:
        SimpleDateFormat SDF = new SimpleDateFormat("yyyy-MM-dd");
        Date TGL = null;
        int baris = tabel_client.getSelectedRow();
        try {
             TGL = SDF.parse((String) tabel_client.getValueAt(baris, 6));
             } catch (ParseException ex) {
                Logger.getLogger(Pengeluaran.class.getName()).log(Level.SEVERE, null, ex);
            }
        txtid.setText(tabel_client.getModel().getValueAt(baris, 0).toString());
        txtnama.setText(tabel_client.getModel().getValueAt(baris, 1).toString());
        txtalamat.setText(tabel_client.getModel().getValueAt(baris, 2).toString());
        cbagama.setSelectedItem(tabel_client.getModel().getValueAt(baris, 3).toString());
        txtemail.setText(tabel_client.getModel().getValueAt(baris, 4).toString());
        txttelp.setText(tabel_client.getModel().getValueAt(baris, 5).toString());
        date.setDate(TGL);
    }//GEN-LAST:event_tabel_clientMouseClicked

