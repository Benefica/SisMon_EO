    public void tabel_client(){
        Object header[]={"ID CLIENT","NAMA CLIENT","ALAMAT","AGAMA","EMAIL","NO TELP","TANGGAL INPUT"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        tabel_client.setModel(data);
        setKoneksi();
        String sql="select*from data_client";
        try {
            ResultSet rs=stm.executeQuery(sql);
            while (rs.next())
            {
                String kolom1=rs.getString("id_client");
                String kolom2=rs.getString("nama_client");
                String kolom3=rs.getString("alamat");
                String kolom4=rs.getString("agama");
                String kolom5=rs.getString("email");
                String kolom6=rs.getString("telepon");
                String kolom7=rs.getString("tanggal");
                
                String kolom[]={kolom1,kolom2,kolom3,kolom4,kolom5,kolom6,kolom7};
                data.addRow(kolom);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "data gagal dipanggil"+e);
        }
    }
    
    public void Tampil_ID() {
        try {
            ResultSet rs = stm.executeQuery("SELECT * FROM data_client ORDER BY id_client DESC");

            if (rs.next()) {
                int lastID = Integer.parseInt(rs.getString("id_client").substring(3));
                int newID = lastID + 1;
                String AN = String.format("%04d", newID);
                txtid.setText("CLN" + AN);
            } else {
                txtid.setText("CLN0001");
            }
        } catch (Exception e) {
            txtid.setText("CLN0001");
        }
    }
    
